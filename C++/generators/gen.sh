k=1;
for i in {-1,1} ; 
do 
	for j in {-2,2} ; 
	do 
		echo "$i $j" > inFile$k ; 
		echo -e "ADD:\n$i $j\n$(($i+$j))\n$(($i+$j)) $j\n$(($i+$j+$j))\n---------------------------------------\nSUB:\n$(($i+$j)) $j\n$i\n$i $j\n$(($i-$j))\n---------------------------------------\nMUL:\n$i $j\n$(($i*$j))\n$(($i*$j)) $j\n$(($i*$j*$j))\n---------------------------------------" > outFile$k; 
		k=$(($k+1)); 
	done; 
done

